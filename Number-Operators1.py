number1 = 13
number2 = 2

add = number1 + number2
print(f'{number1} + {number2} = {add}')
print(type(add))

sub = number1 - number2
print(f'{number1} - {number2} = {sub}')
print(type(sub))

multipli = number1 * number2
print(f'{number1} x {number2} = {multipli}')
print(type(multipli))

div = number1 / number2
print(f'{number1} / {number2} = {div}')
print(type(div))

floor_div = number1 // number2
print(f'{number1} // {number2} = {floor_div}')
print(type(floor_div))

floor_div = 15 // 8
print(f'15 // 8 = {floor_div}')
print(type(floor_div))

expo = number1 ** 4
print(f'{number1} ^ 4 = {expo}')
print(type(expo))

