number1 = 14
number2 = 6

# equal
print(number1 == number2) 

# not equal
print(number1 != number2)

# greater than
print(number1 > number2)

# greater than equal to
print(number1 >= number2)

# less than
print(number1 < number2)

# less than equal to
print(number1 <= number2)
print(type(number1 <= number2))

